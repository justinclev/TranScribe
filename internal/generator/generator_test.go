func TestGenerate_OverwritesExistingFiles(t *testing.T) {
	dir := t.TempDir()
	bp1 := minimalBP()
	bp1.Region = "eu-west-1"
	mustGenerate(t, bp1, dir)

	bp2 := minimalBP()
	bp2.Region = "ap-northeast-1"
	mustGenerate(t, bp2, dir)

	content := readTestFile(t, dir, "main.tf")
	if !strings.Contains(content, `"ap-northeast-1"`) {
		t.Error("main.tf not overwritten with new region")
	}
	if strings.Contains(content, `"eu-west-1"`) {
		t.Error("main.tf still contains stale region after overwrite")
	}
}

