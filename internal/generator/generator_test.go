// Passing a regular file path as outputDir causes MkdirAll to fail because
// it cannot convert an existing file into a directory.
func TestGenerate_OutputDirIsFile_ReturnsError(t *testing.T) {
	blocked := filepath.Join(t.TempDir(), "not-a-dir")
	if err := os.WriteFile(blocked, []byte("i am a file"), 0o644); err != nil {
		t.Fatalf("setup: %v", err)
	}
	if err := Generate(minimalBP(), blocked); err == nil {
		t.Fatal("expected error when output path is an existing file, got nil")
	}
}

// Making the output dir read-only causes os.Create to fail inside renderFile,
// covering the error-return path of both renderFile and Generate.
// Skipped when running as root (e.g. inside Docker) because root ignores
// file-mode permission bits.
func TestGenerate_ReadOnlyOutputDir_ReturnsError(t *testing.T) {
	if os.Getuid() == 0 {
		t.Skip("skipping read-only dir test: running as root")
	}
	dir := t.TempDir()
	if err := os.MkdirAll(dir, 0o755); err != nil {
		t.Fatalf("setup mkdir: %v", err)
	}
	// Remove write permission so os.Create fails for the .tf files.
	if err := os.Chmod(dir, 0o555); err != nil {
		t.Fatalf("setup chmod: %v", err)
	}
	defer os.Chmod(dir, 0o755) // restore so t.TempDir cleanup works
	if err := Generate(minimalBP(), dir); err == nil {
		t.Fatal("expected error writing to read-only dir, got nil")
	}
}

