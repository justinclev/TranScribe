func TestDbPort(t *testing.T) {
	cases := []struct {
		engine models.DatabaseEngine
		want   string
	}{
		{models.EnginePostgres, "5432"},
		{models.EngineAuroraPostgres, "5432"},
		{models.EngineMySQL, "3306"},
		{models.EngineAuroraMySQL, "3306"},
		{models.EngineMariaDB, "3306"},
		{models.EngineSQLServer, "1433"},
		{models.EngineOracle, "1521"},
		{models.EngineDocumentDB, "27017"},
		{models.EngineRedis, "6379"},
		{models.EngineMemcached, "11211"},
		{models.EngineNeptune, "8182"},
		{models.EngineDynamoDB, "5432"}, // unknown → default postgres port
	}
	for _, tc := range cases {
		t.Run(string(tc.engine), func(t *testing.T) {
			if got := dbPort(tc.engine); got != tc.want {
				t.Errorf("dbPort(%s) = %s, want %s", tc.engine, got, tc.want)
			}
		})
	}
}

