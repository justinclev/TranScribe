func TestGenerate_ReturnsErrorForInvalidOutputDir(t *testing.T) {
	bp := minimalBP()
	// A file path where a directory cannot be created (parent is a file).
	tmp := t.TempDir()
	blockingFile := filepath.Join(tmp, "blocker")
	if err := os.WriteFile(blockingFile, []byte("x"), 0o644); err != nil {
		t.Fatal(err)
	}
	err := Generate(bp, filepath.Join(blockingFile, "subdir"))
	if err == nil {
		t.Error("expected error for invalid output dir, got nil")
	}
}

